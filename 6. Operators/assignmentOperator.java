public class assignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        // a = a + 10;
        a+=10;
        int b = 5;
        b-=5;
        int c = 2;
        c*=2;
        int d = 10;
        d/=2;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
