public class arithmeticOperator {
    public static void main(String[] args) {
        /*
         * + --> for sum
         * - --> for substraction
         * * --> for multiplication
         * / --> for division
         * % --> gives remainder
         */

        int a = 10;
        int b = 3;

        System.out.println("Add = " + (a+b));
        System.out.println("Sub = " + (a-b));
        System.out.println("Mul = " + (a*b));
        System.out.println("Div = " + (a/b));
        System.out.println("Mod = " + (a%b));
    }
}
